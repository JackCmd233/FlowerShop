package com.po;

public class Order {
	private Integer id;//订单id
	private Integer busertable_id;//用户id
	private Double amount;//订单金额
	private Integer status;//订单状态
	private String orderdate;//下单时间
	private String payway;//支付方式
	private Integer buseraddress_id;//订单地址id
	private Integer deletestatus;//前台用户是否删除订单
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getBusertable_id() {
		return busertable_id;
	}
	public void setBusertable_id(Integer busertable_id) {
		this.busertable_id = busertable_id;
	}
	public Double getAmount() {
		return amount;
	}
	public void setAmount(Double amount) {
		this.amount = amount;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getOrderdate() {
		return orderdate;
	}
	public void setOrderdate(String orderdate) {
		this.orderdate = orderdate;
	}
	public String getPayway() {
		return payway;
	}
	public void setPayway(String payway) {
		this.payway = payway;
	}
	public Integer getBuseraddress_id() {
		return buseraddress_id;
	}
	public void setBuseraddress_id(Integer buseraddress_id) {
		this.buseraddress_id = buseraddress_id;
	}
	public Integer getDeletestatus() {
		return deletestatus;
	}
	public void setDeletestatus(Integer deletestatus) {
		this.deletestatus = deletestatus;
	}
}
